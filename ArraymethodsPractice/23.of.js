/*
ARRAY OF() METHOD :
The of() method creates a new Array instance from the given arguments.
It just used to create the array.
*/
const arr = Array.of("book", "pen", "pencil", "bag")
console.log(arr); // [ 'book', 'pen', 'pencil', 'bag' ]


const arr1 = Array.of(1,2,3,4,5,6,7,8)
console.log(arr1); // [ 1, 2, 3, 4, 5, 6, 7, 8 ]
