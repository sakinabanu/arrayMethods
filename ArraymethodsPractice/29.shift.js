/*
ARRAY SHIFT() METHOD :
The shift() method removes the first element from an array and returns the updated array.
It removes the first element and returns the array.
*/


const arr = [1, 2, 3, 4, 5];
arr.shift();
console.log(arr); // [ 2, 3, 4, 5 ]

let alpha = ["M", "N", "O","P", "Q", "R"];
alpha.shift();
console.log(alpha); // [ 'N', 'O', 'P', 'Q', 'R' ]